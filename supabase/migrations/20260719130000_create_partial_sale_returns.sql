    ALTER TABLE public.sale_return_items
      ADD CONSTRAINT sale_return_items_negocio_id_fkey
      FOREIGN KEY (negocio_id)
      REFERENCES public.negocios(id)
      ON DELETE CASCADE;
  END IF;
END;
$$;

CREATE INDEX IF NOT EXISTS sale_returns_business_created_idx
  ON public.sale_returns (negocio_id, created_at DESC);

CREATE INDEX IF NOT EXISTS sale_returns_business_date_idx
  ON public.sale_returns (negocio_id, return_date DESC);

CREATE INDEX IF NOT EXISTS sale_returns_sale_idx
  ON public.sale_returns (sale_id, created_at DESC);

CREATE INDEX IF NOT EXISTS sale_return_items_sale_item_idx
  ON public.sale_return_items (sale_item_id);

CREATE INDEX IF NOT EXISTS sale_return_items_business_idx
  ON public.sale_return_items (negocio_id);

ALTER TABLE public.sale_returns ENABLE ROW LEVEL SECURITY;
