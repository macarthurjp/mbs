/*
  Partial sale returns keep the original invoice immutable while recording
  stock disposition and customer compensation in one atomic operation.
*/

CREATE TABLE IF NOT EXISTS public.sale_returns (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  negocio_id uuid NOT NULL REFERENCES public.negocios(id) ON DELETE CASCADE,
  sale_id bigint NOT NULL REFERENCES public.ventas(id) ON DELETE RESTRICT,
  client_id bigint REFERENCES public.clientes(id) ON DELETE SET NULL,
  compensation_method text NOT NULL CHECK (
    compensation_method IN ('cash_refund', 'store_credit', 'reduce_debt')
  ),
  reason text NOT NULL,
  refund_total numeric NOT NULL DEFAULT 0 CHECK (refund_total >= 0),
  restocked_quantity numeric NOT NULL DEFAULT 0 CHECK (restocked_quantity >= 0),
  damaged_quantity numeric NOT NULL DEFAULT 0 CHECK (damaged_quantity >= 0),
  created_by uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  processed_by_name text NOT NULL DEFAULT 'Usuario',
  return_date date NOT NULL DEFAULT current_date,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.sale_return_items (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  negocio_id uuid NOT NULL REFERENCES public.negocios(id) ON DELETE CASCADE,
  return_id bigint NOT NULL REFERENCES public.sale_returns(id) ON DELETE CASCADE,
  sale_item_id bigint NOT NULL REFERENCES public.venta_items(id) ON DELETE RESTRICT,
  product_id bigint REFERENCES public.productos(id) ON DELETE SET NULL,
  product_name text NOT NULL,
  quantity numeric NOT NULL CHECK (quantity > 0),
  stock_condition text NOT NULL CHECK (stock_condition IN ('restock', 'damaged')),
  unit_price numeric NOT NULL CHECK (unit_price >= 0),
  gross_amount numeric NOT NULL CHECK (gross_amount >= 0),
  discount_amount numeric NOT NULL DEFAULT 0 CHECK (discount_amount >= 0),
  refund_amount numeric NOT NULL CHECK (refund_amount >= 0),
  created_at timestamptz NOT NULL DEFAULT now()
);

-- Some environments received an early version of these tables before this
-- migration was tracked. Complete that schema in place without dropping or
-- replacing existing return records.
